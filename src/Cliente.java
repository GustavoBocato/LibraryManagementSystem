public class Cliente {

    private String nome;
    private String cpf;
    private String telefone;
    private String email;
    private String endereço;

    public Cliente(String nome, String cpf, String telefone, String email, String endereço){

        this.nome = nome;
        this.cpf = cpf;
        this.telefone = telefone;
        this.email = email;
        this.endereço = endereço;

    }



}
